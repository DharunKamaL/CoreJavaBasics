package com.basicSamples;

public class DataTypeSamples 
{

	public static void main(String[] args) 
	{
		int a = 1;
		
		double myDouble = 3.4;
		float myFloat = 3.4f;
		double myScientific = 3.445e2;
		char letter = 'a';
		String str1 = "Java Programming";
		String str2 = "Programiz";
		
		byte range = 124;
		short temp = -200;
		int irange = -4250000;
		long lrange = -423322233;
		
		System.out.println("a value:"+a);
		System.out.println("double value:"+myDouble);
		System.out.println("float value:"+myFloat);
		System.out.println("Scientific value:"+myScientific);
		System.out.println("character value:"+letter);
		System.out.println("string value1:"+str1);
		System.out.println("string value2:"+str2);
		System.out.println("byte value:"+range);
		System.out.println("short value:"+temp);
		System.out.println("int value:"+irange);
		System.out.println("long value:"+lrange);
		

	}

}
